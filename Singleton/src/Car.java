
public class Car {
	int year;
	String make;
	double speed;
	String color;
	
	public Car(int year, String make, double speed) {
		super();
		this.year = year;
		this.make = make;
		this.speed = speed;
	}
	public int getYear() {
		return year;
	}
	public String getMake() {
		return make;
	}
	public double getSpeed() {
		return speed;
	}
	public String getColor() {
		return color;
	}
	
}
